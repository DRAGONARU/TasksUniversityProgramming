import random
def game():
    '''
    Функция для игры в виселицу, очень весёлая игра.
    '''
    input('Нажмите Enter для начала игры')
    answer = random.choice(words)
    right_guessed = []
    wrong_guessed = []
    wrong_count = 0
    while wrong_count < 6:
        print('Статус слова')
        for letter in answer:
            if letter in right_guessed:
                print(letter, end='')
            else:
                print('_', end='')
        print()
        print(f'Угаданы буквы: {right_guessed}')
        print(f'Неправильно угаданные буквы: {wrong_guessed}')
        flag_guessed = False
        while not flag_guessed:
            guess = input('Введите букву: ')
            if guess in wrong_guessed or guess in right_guessed:
                print('Вы уже вводили эту букву')
            elif len(guess) == 1:
                flag_guessed = True
            else:
                print('Неверный ввод')
        if guess in answer:
            print('Буква есть в слове')
            right_guessed.append(guess)
        else:
            print('Буквы нет в слове')
            wrong_guessed.append(guess)
            wrong_count += 1
        print(f'Осталось попыток: {6 - wrong_count}')
        if set(right_guessed) == set(answer):
            print('Поздравляем! Вы выиграли!')
            return 0
    print(f'Вы проиграли. Было загадано слово {answer}')
    return 0



